export const ALL_CARDS: TarotCard[] = [...MAJOR_ARCANA, ...MINOR_ARCANA];

export function getCardById(id: string): TarotCard | undefined {
  return ALL_CARDS.find((c) => c.id === id);
}

// Fisher-Yates shuffle
export function shuffleDeck<T>(deck: T[]): T[] {
  const a = [...deck];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function drawRandomCards(n: number): { card: TarotCard; reversed: boolean }[] {
  const shuffled = shuffleDeck(ALL_CARDS);
  return shuffled.slice(0, n).map((card) => ({
    card,
    reversed: Math.random() < 0.3,
  }));
}
